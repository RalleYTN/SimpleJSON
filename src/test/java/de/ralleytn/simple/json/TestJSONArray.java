	public void testEquals() {
		
		Float[] nullArray1 = new Float[] {null, null, null, null, null};
		Float[] nullArray2 = new Float[] {null, null, null, null, null};
		
		JSONArray jsonNullArray1 = new JSONArray(nullArray1);
		JSONArray jsonNullArray2 = new JSONArray(nullArray2);
		
		float[] arrayCorrect = {0, 1, 2, 3, 4};
		List<Float> listCorrect = Arrays.asList(0F, 1F, 2F, 3F, 4F);
		JSONArray jsonArrayCorrect = new JSONArray(arrayCorrect);
		
		float[] arrayIncorrect = {71, 389, 21, 8, 9};
		List<Float> listIncorrect = Arrays.asList(0F);
		JSONArray jsonArrayIncorrect = new JSONArray(arrayIncorrect);
		
		JSONArray array = new JSONArray();
		array.add(0.0F);
		array.add(1.0F);
		array.add(2.0F);
		array.add(3.0F);
		array.add(4.0F);
		
		assertTrue(array.equals(arrayCorrect));
		assertTrue(array.equals(listCorrect));
		assertTrue(array.equals(jsonArrayCorrect));
		assertTrue(jsonNullArray1.equals(jsonNullArray2));
		assertTrue(jsonNullArray1.equals(nullArray1));

		assertFalse(array.equals(arrayIncorrect));
		assertFalse(array.equals(listIncorrect));
		assertFalse(array.equals(jsonArrayIncorrect));
		assertFalse(array.equals(null));
		assertFalse(array.equals(jsonNullArray1));
		assertFalse(array.equals(jsonArrayIncorrect.toString()));
		assertFalse(array.equals(new float[] {71, 389, 21, 8}));
		assertFalse(array.equals(nullArray1));
	}
	
